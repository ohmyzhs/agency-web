export function getWordsForStage(stage: StageLevel): string[] {
  return koreanWordsByStage[stage] ?? koreanWordsByStage[400];
}

export function getSentencesForStage(stage: StageLevel): string[] {
  return [
    ...(koreanSentencesByStage[stage] ?? koreanSentencesByStage[400]),
    ...(koreanSentenceExpansion[stage] ?? koreanSentenceExpansion[400]),
  ];
}

export function getPassagesForCategory(category: LongformCategory, language: 'ko' | 'en' = 'ko'): PassageItem[] {
  if (category === 'AI단편' && language === 'en') {
    return englishAiShortStoryPassages;
  }
  if (REMOVED_SHORT_LONGFORM_CATEGORIES.has(category)) return [];
  return (koreanPassagesByCategory[category] ?? []).map(expandPassageToPracticeLength);
}

export function pickRandom<T>(arr: T[]): T {
  return arr[Math.floor(Math.random() * arr.length)];
}

export function buildWordStreamFromStage(stage: StageLevel, count = 25): string {
  const pool = getWordsForStage(stage);
  const words: string[] = [];
  for (let i = 0; i < count; i++) {
    words.push(pool[Math.floor(Math.random() * pool.length)]);
  }
  return words.join(' ');
}

