const LONGFORM_MIN_CHARS = 850;
const LONGFORM_TARGET_CHARS = 1100;
const REMOVED_SHORT_LONGFORM_CATEGORIES = new Set<LongformCategory>(["애국가", "속담", "명언"]);

function expandPassageToPracticeLength(item: PassageItem): PassageItem {
  if (item.text.length >= LONGFORM_MIN_CHARS) return item;
  const seed = item.text.trim();
  const sentences = [
    seed,
    "이 지문은 한 호흡으로 끝나는 짧은 문장이 아니라, 손가락의 리듬과 시선 이동을 함께 연습할 수 있도록 같은 주제를 조금 더 길게 이어 갑니다.",
    "처음에는 속도보다 정확도를 우선하고, 문장 사이의 쉼표와 마침표를 놓치지 않으며, 화면의 흐름을 따라 천천히 입력하는 편이 좋습니다.",
    "장문 연습의 목적은 단순히 빠른 타수를 얻는 것이 아니라, 긴 글을 읽고 이해하면서도 손이 자연스럽게 다음 글자를 찾아가도록 만드는 데 있습니다.",
    "따라서 이 글은 원문의 핵심 내용을 보존하되, 필사 연습에 필요한 분량을 확보하기 위해 배경 설명과 연습 안내를 덧붙인 자체 확장 지문입니다.",
    "반복해서 입력하다 보면 특정 자모에서 자주 멈추는 지점, 띄어쓰기에서 흔들리는 지점, 문장 부호를 지나칠 때 생기는 습관을 더 분명하게 확인할 수 있습니다.",
  ];
  let text = sentences.join(" ");
  while (text.length < LONGFORM_TARGET_CHARS) {
    text += ` ${seed}`;
  }
  return { ...item, text };
}

